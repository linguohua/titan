// DB Persistent db
type DB interface {
	IsNilErr(err error) bool
}

var db DB

// NotFind not find data
const NotFind = "not find"

// NewDB New  DB
func NewDB(url string, dbType string) error {
	var err error

	switch dbType {
	case TypeSQL():
		db, err = InitSQL(url)
	default:
		// panic("unknown DB type")
		err = xerrors.New("unknown DB type")
	}

	// if err != nil {
	// 	eStr = fmt.Sprintf("NewDB err:%v , url:%v", err.Error(), url)
	// 	// panic(e)
	// }

	return err
}

// GetDB Get DB
func GetDB() DB {
	return db
}

